from main import HF_API_KEY, HF_API_URL
import requests



def response_ai(text: str) -> str:
    prompt = f"""
    Ты — опытный преподаватель английского языка и методист CEFR.

    Твоя задача — ЧЕСТНО определить уровень английского студента по его тексту.

    ⚠️ КРИТИЧЕСКИ ВАЖНО:
    — НЕ завышай уровень
    — Если есть сомнения между уровнями — выбирай НИЖЕ
    — Короткий, простой, шаблонный текст = вижу A1 или A2
    — Ошибки в базовой грамматике (to be, времена, порядок слов) = НЕ выше A2
    — Свободное владение временами + связная речь = B1 и выше

    📌 ОРИЕНТИРЫ:
    • A1 — простые предложения, базовая лексика, много ошибок
    • A2 — короткие связные мысли, ошибки в временах
    • B1 — уверенная структура, простая аргументация
    • B2+ — сложные конструкции, минимум базовых ошибок

    🎯 МОТИВАЦИЯ:
    — Будь поддерживающим
    — НЕ льсти
    — Хвали только то, что реально есть

    ---

    ОТВЕЧАЙ СТРОГО В ФОРМАТЕ:

    📊 Результат оценки:

    Ваш примерный уровень: <УРОВЕНЬ> (<НАЗВАНИЕ>)

    Что хорошо:
    ✔️ <реальное сильное место>
    ✔️ <реальное сильное место>

    Что стоит улучшить:
    • <конкретная проблема>
    • <конкретная проблема>

    💡 С таким уровнем вы уже можете:
    — <реалистичная возможность>
    — <реалистичная возможность>

    ---

    Текст студента:
    {text}
    """

    headers = {
        "Authorization": f"Bearer {HF_API_KEY}",
        "Content-Type": "application/json"
    }

    data = {
        "model": "meta-llama/Meta-Llama-3-8B-Instruct",
        "messages": [
            {"role": "user", "content": prompt}
        ],
        "temperature": 0.3
    }

    response = requests.post(HF_API_URL, headers=headers, json=data)
    response.raise_for_status()

    return response.json()["choices"][0]["message"]["content"]


